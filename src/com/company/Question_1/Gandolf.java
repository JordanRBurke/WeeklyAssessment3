package com.company.Question_1;

public class Gandolf {

    // Gandolf customization
    int gandolfAge;
    String gandolfStaffName;

    public Gandolf(int gandolfAge, String gandolfStaffName) {
        this.gandolfAge = gandolfAge;
        this.gandolfStaffName = gandolfStaffName;
    }

    public Gandolf() {
    }

    public int getGandolfAge() {
        return gandolfAge;
    }

    public void setGandolfAge(int gandolfAge) {
        this.gandolfAge = gandolfAge;
    }

    public String getGandolfStaffName() {
        return gandolfStaffName;
    }

    public void setGandolfStaffName(String gandolfStaffName) {
        this.gandolfStaffName = gandolfStaffName;
    }
}
